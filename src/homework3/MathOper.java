package homework3;

public class MathOper {
    double x;
    double y;
    public double add(){
        double result = x + y;
        return result;
    }

    public double sub(){
        double result = x - y;
        return result;
    }

    public double mult(){
        double result = x * y;
        return result;
    }

    public double div(){

        return x / y;
    }

}

